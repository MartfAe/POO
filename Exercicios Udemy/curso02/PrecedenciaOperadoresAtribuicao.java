public class PrecedenciaOperadoresAtribuicao {
    public static void main(String[] args) {
        int numero = 10;
        System.out.println("Valor inicial de numero: " + numero);

        System.out.println("Valor pré incrementado:" + (++numero));
        System.out.println("Valor pós incrementado: " + (numero++));
        System.out.println("Valor atual de numero: " + numero);
        System.out.println("Valor pré decrementado: " + (--numero));
        System.out.println("Valor pós decrementado: " + (numero--));
        System.out.println("Valor atual de numero: " + numero);


    }

}
